-- ~/.config/nvim/lua/core/keymaps.lua

-- 定义一个本地变量，简化后续代码
local map = vim.keymap.set

-- =====================================================================
-- 1. 窗口管理 (完美替代繁琐的 Ctrl + w)
-- =====================================================================
-- 让你在左侧文件树、终端和右侧代码区之间光速穿梭
map("n", "<C-h>", "<C-w>h", { desc = "跳转到左侧窗口" })
map("n", "<C-j>", "<C-w>j", { desc = "跳转到下方窗口" })
map("n", "<C-k>", "<C-w>k", { desc = "跳转到上方窗口" })
map("n", "<C-l>", "<C-w>l", { desc = "跳转到右侧窗口" })

-- 调整窗口大小 (按住 Ctrl 和方向键，极其直观)
map("n", "<C-Up>", "<cmd>resize +2<cr>", { desc = "增加窗口高度" })
map("n", "<C-Down>", "<cmd>resize -2<cr>", { desc = "减少窗口高度" })
map("n", "<C-Left>", "<cmd>vertical resize -2<cr>", { desc = "减少窗口宽度" })
map("n", "<C-Right>", "<cmd>vertical resize +2<cr>", { desc = "增加窗口宽度" })

-- =====================================================================
-- 2. 代码块移动 (极度好用的神仙按键)
-- =====================================================================
-- 在可视模式下选中一段代码，按大写 J 或 K，可以把整块代码上下移动，并自动对齐缩进！
map("v", "J", ":m '>+1<CR>gv=gv", { desc = "将选中代码块向下移动" })
map("v", "K", ":m '<-2<CR>gv=gv", { desc = "将选中代码块向上移动" })

-- =====================================================================
-- 3. 视觉体验优化 (保持光标居中)
-- =====================================================================
-- 按 Ctrl+d / Ctrl+u 上下翻页时，让光标始终保持在屏幕正中间，防止视觉迷失
map("n", "<C-d>", "<C-d>zz", { desc = "向下翻页并居中" })
map("n", "<C-u>", "<C-u>zz", { desc = "向上翻页并居中" })

-- 搜索时 (按 n 找下一个)，同样让匹配结果保持在屏幕正中间
map("n", "n", "nzzzv", { desc = "下一个搜索结果并居中" })
map("n", "N", "Nzzzv", { desc = "上一个搜索结果并居中" })

-- =====================================================================
-- 4. 实用小工具
-- =====================================================================
-- Neovim 默认搜索完后，高亮不会消失。按 Esc 键清除搜索高亮
map("n", "<Esc>", "<cmd>nohlsearch<CR>", { desc = "清除搜索高亮" })

-- 快速保存和退出 (如果你不习惯 :w 和 :q)
map("n", "<leader>w", "<cmd>w<CR>", { desc = "保存当前文件" })

-- 在光标下方插入空行，但不进入插入模式 (非常好用的排版技巧)
map("n", "<leader>o", "o<Esc>", { desc = "在下方插入空行" })
map("n", "<leader>O", "O<Esc>", { desc = "在上方插入空行" })
